''' 
Python = linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
'''
print(1234)

# Aspas simples
print('Natã Andretto')
print("'Natã Andretto'") # As aspas simples iram aparecer.

# Aspas duplas
print("Natã Andretto")
print('"Natã Andretto"') # As aspas duplas iram aparecer.

# Escape
print('Natã \'Andretto\'')

# r
print(r'Natã \'Andretto\'')
